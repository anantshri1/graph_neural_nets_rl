def collect_imitation_data(n_games=200, verbose=True):
    """
    Run n_games of BeamSearch vs BeamSearch and record
    (feature_vector, source_id, angle, ship_count_norm) at every turn.

    Returns a list of dicts, one per turn per game.
    """
    global _mcts_env

    dataset = []

    for game_idx in range(n_games):
        # fresh environment for each game
        env = make("orbit_wars", debug=False)
        env.reset()

        # point the global env used inside beam_search_agent to this game's env
        _mcts_env = env

        done = False
        while not done:
            # collect action and features for player 0
            obs0  = to_obs(env.state[0].observation)
            act0  = beam_search_agent(obs0, None)

            # collect action and features for player 1
            _mcts_env = env
            obs1  = to_obs(env.state[1].observation)
            act1  = beam_search_agent(obs1, None)

            # skip turns where a player has no valid moves (e.g. lost all planets)
            if not act0 or not act1:
                env.step([act0, act1])
                done = (env.state[0].status != "ACTIVE")
                continue

            # now safe to unpack
            feat0 = obs_to_features(obs0, my_player_id=0)
            src0, angle0, ships0 = act0[0]

            feat1 = obs_to_features(obs1, my_player_id=1)
            src1, angle1, ships1 = act1[0]

            # log both players' turns as separate training samples
            dataset.append({
                "features":       feat0,
                "source_id":      int(src0),
                "angle":          float(angle0),
                "ship_count_norm": float(ships0) / MAX_SHIPS,
            })
            dataset.append({
                "features":       feat1,
                "source_id":      int(src1),
                "angle":          float(angle1),
                "ship_count_norm": float(ships1) / MAX_SHIPS,
            })

            # step the environment
            state = env.step([act0, act1])
            done  = (env.state[0].status != "ACTIVE")

        if verbose and (game_idx + 1) % 10 == 0:
            print(f"Collected {game_idx + 1}/{n_games} games — {len(dataset)} samples so far")

    return dataset


