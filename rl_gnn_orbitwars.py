def hits_sun(x1, y1, x2, y2, sun_x=50, sun_y=50, sun_radius=10):
    """
    Returns True if the line segment from (x1,y1) to (x2,y2)
    passes within sun_radius of the sun center.

    Approach: find the closest point on the segment to the sun,
    then check if that distance < sun_radius.
    """
    # Vector from A to B
    dx, dy = x2 - x1, y2 - y1

    # Vector from A to sun
    fx, fy = sun_x - x1, sun_y - y1

    # t = projection of A->sun onto A->B, clamped to [0,1]
    # t=0 means closest point is A, t=1 means closest point is B
    seg_len_sq = dx*dx + dy*dy
    if seg_len_sq == 0:
        # Source and target are the same point
        dist = math.sqrt(fx*fx + fy*fy)
        return dist < sun_radius

    t = max(0.0, min(1.0, (fx*dx + fy*dy) / seg_len_sq))

    # Closest point on segment to sun
    closest_x = x1 + t * dx
    closest_y = y1 + t * dy

    # Distance from closest point to sun center
    dist = math.sqrt((closest_x - sun_x)**2 + (closest_y - sun_y)**2)

    return dist < sun_radius

