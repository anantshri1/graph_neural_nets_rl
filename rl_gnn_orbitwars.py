def evaluate_position(obs, my_player_id):
    """
    Heuristic evaluation of a game state for my_player_id.
    Returns a float in [0, 1] — higher is better for my_player_id.

    Uses ship count only (planets + in-transit fleets).
    No production weighting — consistent with score_target findings.
    """
    planets = [Planet(*p) for p in obs.planets]

    my_ships = sum(p.ships for p in planets if p.owner == my_player_id)
    enemy_ships = sum(p.ships for p in planets if p.owner != my_player_id and p.owner != -1)

    if hasattr(obs, 'fleets') and obs.fleets:
        for f in obs.fleets:
            if f[1] == my_player_id:
                my_ships += f[5]
            elif f[1] != -1:
                enemy_ships += f[5]

    total = my_ships + enemy_ships
    if total == 0:
        return 0.5

    return my_ships / total

import time
import copy
def beam_search_agent(obs, config):
    """
    Beam search agent with depth=1, beam_width=3.

    Candidates are pre-ranked by score_target and capped at K=3
    before any simulation — keeps time per turn bounded regardless
    of how many planets we own.
    """
    global _mcts_env

    K = 3
    my_player_id = obs.player
    opp_player_id = 1 - my_player_id
    start_time = time.time()

    # sync global env to true game state
    sync_env_to_obs(_mcts_env, obs)

    # generate and pre-rank candidates by score_target — no deepcopy yet
    candidate_actions = _get_candidate_actions(obs)
    if not candidate_actions:
        return heuristic_agent(obs, config)

    # pre-score each candidate cheaply using score_target
    planets = [Planet(*p) for p in obs.planets]
    my_planets = {p.id: p for p in planets if p.owner == my_player_id}
    other_planets = {p.id: p for p in planets if p.owner != my_player_id}

    prescored = []
    for action in candidate_actions:
        src_id = action[0][0]
        src = my_planets.get(src_id)
        if src is None:
            continue
        # score against best available target
        best = max(
            score_target(src, tgt, my_player_id, obs.step,
                        obs.initial_planets, obs.angular_velocity)
            for tgt in other_planets.values()
        )
        prescored.append((best, action))

    prescored.sort(reverse=True)
    top_candidates = [action for _, action in prescored[:K]]

    # simulate only top K candidates
    scored = []
    for action in top_candidates:
        sim_env = copy.deepcopy(_mcts_env)
        opp_obs = to_obs(sim_env.state[opp_player_id].observation)
        opp_action = heuristic_agent(opp_obs, None)

        if my_player_id == 0:
            sim_env.step([action, opp_action])
        else:
            sim_env.step([opp_action, action])

        result_obs = to_obs(sim_env.state[my_player_id].observation)
        score = evaluate_position(result_obs, my_player_id)
        scored.append((score, action))

    scored.sort(reverse=True)
    best_score, best_action = scored[0]

    #print(f"Turn {obs.step} — beam: {len(candidate_actions)} → {len(top_candidates)} candidates, "
          #f"best score: {best_score:.3f}, time: {time.time()-start_time:.3f}s")
    #print(f"best_action: {best_action}")
    #return best_action

    return best_action

