def comet_position(comet_group, comet_idx, future_turn):
    """
    Returns (x, y) of a specific comet at future_turn, or None if off the board.

    comet_group : one entry from obs.comets
    comet_idx   : which comet in the group (0-3, matches planet_ids order)
    future_turn : turns from NOW (0 = current position)
    """
    path = comet_group["paths"][comet_idx]
    current_index = comet_group["path_index"]

    target_index = current_index + future_turn

    if target_index >= len(path):
        return None   # comet has left the board

    x, y = path[target_index]
    return (x, y)

