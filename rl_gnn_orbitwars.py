# ── fleet_speed ──────────────────────────────────────────────────────────────
def fleet_speed(num_ships, max_speed=6.0):
    """
    Returns units/turn for a fleet of num_ships.
    Formula from doc: 1.0 + (maxSpeed - 1.0) * (log(ships) / log(1000)) ^ 1.5
    Edge case: 0 or negative ships returns minimum speed 1.0
    """
    if num_ships <= 1:
        return 1.0
    ratio = math.log(num_ships) / math.log(1000)
    return 1.0 + (max_speed - 1.0) * (ratio ** 1.5)


# ── travel_time ──────────────────────────────────────────────────────────────
def travel_time(x1, y1, x2, y2, num_ships):
    """
    Returns turns (int, rounded up) for a fleet to travel from (x1,y1) to (x2,y2).
    Uses Euclidean distance divided by fleet_speed.
    """
    dist = math.sqrt((x2 - x1)**2 + (y2 - y1)**2)
    speed = fleet_speed(num_ships)
    return math.ceil(dist / speed)

