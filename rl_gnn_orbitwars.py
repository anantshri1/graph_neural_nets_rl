def intercept_point(source_x, source_y, target_planet_id, num_ships,
                    current_turn, initial_planets, angular_velocity):
    """
    Returns (x, y, eta) — where to aim and how long the fleet will take.

    Iterative approach:
      1. Guess ETA using current planet position
      2. Predict where planet will be at current_turn + ETA
      3. Recompute ETA using that new position
      4. Repeat until ETA stops changing (converges in ~3-5 iterations)
    """
    # Start with current planet position as first guess
    x, y = planet_position(target_planet_id, current_turn,
                            initial_planets, angular_velocity)

    eta = travel_time(source_x, source_y, x, y, num_ships)

    for _ in range(10):   # 10 iterations is more than enough
        # Predict planet position at arrival
        x, y = planet_position(target_planet_id, current_turn + eta,
                                initial_planets, angular_velocity)

        new_eta = travel_time(source_x, source_y, x, y, num_ships)

        if new_eta == eta:
            break   # converged

        eta = new_eta

    return (x, y, eta)

