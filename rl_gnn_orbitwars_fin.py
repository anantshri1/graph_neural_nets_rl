def mlp_agent(obs, config):
    my_player_id = obs.player

    # parse planets
    planets = [Planet(*p) for p in obs.planets]
    planets.sort(key=lambda p: p.id)

    # fallback for non-32-planet games
    if len(planets) != 32:
        return heuristic_agent(obs, config)

    my_planets = {p.id: p for p in planets if p.owner == my_player_id}
    if not my_planets:
        return []

    # build feature vector
    features = obs_to_features(obs, my_player_id)
    x = features.reshape(1, -1)  # batch dim

    # forward pass
    source_probs, sin_pred, cos_pred, ships_pred = model.predict(x, verbose=0)

    # mask source softmax — zero out planets we don't own, renormalize
    source_probs = source_probs[0].copy()  # shape (32,)
    mask = np.zeros(32, dtype=np.float32)
    for pid in my_planets:
        mask[pid] = 1.0
    source_probs *= mask
    if source_probs.sum() == 0:
        return heuristic_agent(obs, config)
    source_probs /= source_probs.sum()

    # pick source planet
    source_id = int(np.argmax(source_probs))
    source_planet = my_planets[source_id]

    # recover angle from sin/cos
    angle = float(np.arctan2(sin_pred[0][0], cos_pred[0][0]))

    # recover ship count and clip
    ships_raw = float(ships_pred[0][0]) * MAX_SHIPS
    ships = int(np.clip(ships_raw, 1, source_planet.ships))

    return [[source_id, angle, ships]]

