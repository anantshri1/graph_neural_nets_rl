"""# **Key Strategy: Beam Search**

**Initial Strategy: Monte Carlo Tree Search** (MCTS) *Lite*

*Explanation for dummies (me)*:

A regular heuristic agent asks: *"what's the best action right now?"*
MCTS asks: **"if I play this action, and then both sides play reasonably for the rest of the game, who wins?"**

It does this by building a tree of possible futures, one node per game state. The four steps that repeat every iteration:

1. **Selection** — starting from the root (current game state), walk down the tree picking the most promising node at each level. "Most promising" balances exploitation (nodes that have won before) vs exploration (nodes we haven't tried much). This is the UCB1 formula:

<center>
$UCB1 = \frac{w}{n}+C\sqrt{\frac{ln N}{n}}, $
</center>
where: $\frac{w}{n}$: win rate of the node (exploitation), $N$: visits to the parent, $n$: visits to this node (exploration), $C$: exploration constant (typically $\sqrt{2}$).

2. **Expansion** - at a leaf node (one we haven't explored yet), add its children (one per candidate action).

3. **Simulation** - from the new node, play the game to completion using our heuristic agent for both sides. Record who wins.

4. **Backpropagation** - walk back up the tree, updating each node's win count and visit count.

> Refer arXiv:2103.04931v4 [cs.AI] for more details

> Why this doesn't work? **Long** simulation times, never converged in the length of the game.

**Actual Strategy: Beam Search**

*Explanation for dummies (me)*

At each decision point, instead of exploring one action (greedy) or all actions (full tree), you keep the top K candidates — called the "beam width". You expand only those K, evaluate their children, keep the top K again, and repeat for a fixed depth.

At each level you prune ruthlessly — only the top K survive regardless of which parent they came from.

MCTS needs many iterations to build up reliable visit counts before UCB1 means anything. With only 4-10 iterations you're essentially random.
Beam search makes no such assumption — it does one pass, evaluates every node once, and picks the best. With a fixed K and depth D you know exactly how many deepcopy calls you'll make: K × D. Predictable, budgetable.

> Beam search can miss the best action if it gets pruned early — it's not guaranteed optimal like full tree search. But in practice for fast games with tight time budgets it tends to outperform broken MCTS significantly.

> No significant difference between D = 1 or D = 2; to keep computation costs low, we set D = 1
"""

#-------------- Helper function --------------------------------
def to_obs(raw_obs):
    """
    Converts a Struct or dict observation into a SimpleNamespace
    so we can always use dot notation safely.
    """
    from types import SimpleNamespace
    if isinstance(raw_obs, dict):
        ns = SimpleNamespace(**raw_obs)
        if not hasattr(ns, 'step'):
            ns.step = raw_obs.get('step', 0)
        return ns
    return raw_obs

#-------------- Helper function --------------------------------
def _get_candidate_actions(obs):
    """
    Generates diverse candidate actions by varying the TARGET planet.
    For each owned planet, considers its top-3 targets by score_target().
    """
    candidates = []
    seen = set()

    planets = [Planet(p[0],p[1],p[2],p[3],p[4],p[5],p[6]) for p in obs.planets]
    my_planets = [p for p in planets if p.owner == obs.player]
    other_planets = [p for p in planets if p.owner != obs.player]

    if not my_planets or not other_planets:
        return candidates

    TOP_TARGETS = 3
    SHIP_FRACTION = 0.7

    for src in my_planets:
        ships_to_send = max(1, int(src.ships * SHIP_FRACTION))

        scored = []
        for tgt in other_planets:
            s = score_target(src, tgt, obs.player, obs.step, obs.initial_planets, obs.angular_velocity)
            scored.append((s, tgt))

        scored.sort(reverse=True)
        top_targets = [tgt for _, tgt in scored[:TOP_TARGETS]]

        for tgt in top_targets:
            ix, iy, eta = intercept_point(
                src.x, src.y, tgt.id, ships_to_send,
                obs.step, obs.initial_planets, obs.angular_velocity
            )
            angle = safe_angle(src.x, src.y, ix, iy)
            if angle is None or ships_to_send <= 0:
                continue

            action = [[src.id, angle, ships_to_send]]
            key = (src.id, round(angle, 3))
            if key not in seen:
                seen.add(key)
                candidates.append(action)

    return candidates

