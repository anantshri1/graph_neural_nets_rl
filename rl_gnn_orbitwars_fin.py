"""This is basically entropy collapse to a static heuristic mask for the target.

**Problem**: BCE is learning feasibility, not decision-making; Policy needs conditional choice given source.

Essentially, what we want is:
```
P(src,tgt∣state)
```

CE trains `P(src|state)`, BCE trains `P(tgt_valid|state)` BUT we miss `P(tgt|src, state)`. Essentially, the policy head learns *global affordance map*, not *best response to chosen source*.
"""

