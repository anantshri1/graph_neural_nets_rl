def generate_warmup_data(n_samples=2000):
    """
    Generate synthetic (obs, source_id, target_id) triples.

    For each sample:
      - reset a fresh game
      - pick a random owned planet as source
      - pick a random unowned planet as target
      - record the 243-dim feature vector

    No gameplay — just initial game states.
    Returns a list of dicts.
    """
    dataset = []

    for i in range(n_samples):
        # fresh game state
        env = make("orbit_wars", debug=False)
        env.reset()
        obs = to_obs(env.state[0].observation)

        planets = [Planet(*p) for p in obs.planets]
        my_planets  = [p for p in planets if p.owner == 0]
        opp_planets = [p for p in planets if p.owner != 0]

        # skip if no valid source or target
        if not my_planets or not opp_planets:
            continue

        source = random.choice(my_planets)
        target = random.choice(opp_planets)

        features = obs_to_features(obs, my_player_id=0)

        dataset.append({
            "features"  : features,
            "source_id" : source.id,
            "target_id" : target.id,
        })

    return dataset

