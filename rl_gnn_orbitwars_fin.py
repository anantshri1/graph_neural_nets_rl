def planet_position(planet_id, future_turn, initial_planets, angular_velocity):
    """
    Returns (x, y) of a planet at future_turn.

    Static planets (orbital_radius + planet_radius >= 50) don't move — return initial pos.
    Orbiting planets rotate around sun (50, 50) by angular_velocity each turn.

    planet_id       : int
    future_turn     : int, how many turns from now (0 = current)
    initial_planets : raw list from obs.initial_planets
    angular_velocity: float from obs.angular_velocity
    """
    SUN_X, SUN_Y = 50, 50

    # Find the planet in initial_planets by id
    raw = next(p for p in initial_planets if p[0] == planet_id)
    pid, owner, x0, y0, radius, ships, production = raw

    # Orbital radius = distance from sun at game start
    orbital_radius = math.sqrt((x0 - SUN_X)**2 + (y0 - SUN_Y)**2)

    # Static check: doc says orbiting only if orbital_radius + radius < 50
    if orbital_radius + radius >= 50:
        return (x0, y0)   # doesn't move

    # Initial angle of planet relative to sun
    initial_angle = math.atan2(y0 - SUN_Y, x0 - SUN_X)

    # Angle at future_turn
    angle_at_t = initial_angle + angular_velocity * future_turn

    # New position
    x = SUN_X + orbital_radius * math.cos(angle_at_t)
    y = SUN_Y + orbital_radius * math.sin(angle_at_t)

    return (x, y)

