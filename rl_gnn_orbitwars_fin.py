import numpy as np

MAX_SHIPS = 500.0   # normalization constant
MAX_STEP  = 400.0   # typical game length
MAP_SIZE  = 100.0   # x, y lie in roughly [0, 100]

def obs_to_features(obs, my_player_id):
    """
    Convert a raw observation into a flat numpy feature vector.

    Planet features (per planet, sorted by planet id):
        x, y               — raw coordinates (the GNN will use relative positions later;
                             for the MLP we keep absolute for simplicity)
        owner_encoded      — 0: neutral, 1: me, 2: opponent
        ships_norm         — ships / MAX_SHIPS
        production_norm    — production / max_production (computed from initial_planets)

    Global features (appended at the end):
        step_norm          — obs.step / MAX_STEP
        my_ships_norm      — total my ships / MAX_SHIPS
        opp_ships_norm     — total opponent ships / MAX_SHIPS
    """
    planets = [Planet(*p) for p in obs.planets]
    planets.sort(key=lambda p: p.id)   # consistent ordering

    max_production = max(p.production for p in planets) or 1.0

    planet_features = []
    my_total_ships  = 0.0
    opp_total_ships = 0.0

    for p in planets:
        # encode owner relative to current player
        if p.owner == -1:
            owner_enc = 0        # neutral
        elif p.owner == my_player_id:
            owner_enc = 1        # mine
        else:
            owner_enc = 2        # opponent

        planet_features.extend([
            p.x/ MAP_SIZE,
            p.y/ MAP_SIZE,
            owner_enc,
            p.ships / MAX_SHIPS,
            p.production / max_production,
        ])

        if p.owner == my_player_id:
            my_total_ships += p.ships
        elif p.owner != -1:
            opp_total_ships += p.ships

    global_features = [
        obs.step / MAX_STEP,
        my_total_ships  / MAX_SHIPS,
        opp_total_ships / MAX_SHIPS,
    ]

    return np.array(planet_features + global_features, dtype=np.float32)

