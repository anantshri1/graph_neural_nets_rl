"""After the encoder runs, we have `h`: a matrix of shape `[N, 64]` — one 64-dimensional embedding per planet. The network now needs to answer two questions:

* **Policy head (actor)** : *"which action should I take?"* In our `MultiDiscrete([32, 32, 10])` space this means producing three independent probability distributions — one over source planets, one over target planets, one over ship fraction bins.
* **Value head (critic)**: *"how good is the current state overall?"* This needs to be a single scalar — not per-planet, but a global assessment. We get there by global mean pooling: average all node embeddings into one vector, then pass through an MLP. This is the standard approach and it's permutation-invariant by construction — the mean of a set doesn't depend on ordering.

The policy head is more interesting. We have N planet embeddings but a fixed 40-slot action space. The way to handle this cleanly:

* For source and target: learn a linear projection `[64 → 1]` that scores each node. This gives `[N, 1]` scores. Pad to [40, 1] (zeros for missing slots) and apply softmax. The network learns "what makes a planet a good source / good target" as a scoring function over embeddings.
* For ship fraction: global mean pool → small MLP → 10 logits. Ship fraction is a global decision (how aggressive to be right now), not a per-planet one.
"""

# ── Block 3: Policy and Value Heads ──────────────────────────────────────────

import torch
import torch.nn as nn
import torch.nn.functional as F

N_PLANETS   = 40
N_SHIP_BINS = 10

class OrbitWarsPolicyHeads(nn.Module):
    """
    Takes node embeddings from the GNN encoder and produces:
      - source_logits : [40]  — score for each planet slot as source
      - target_logits : [40]  — score for each planet slot as target
      - ship_logits   : [10]  — score for each ship fraction bin
      - value         : [1]   — scalar state value for the critic

    All logits are raw (pre-softmax) — SB3's distribution layer handles
    the softmax + sampling internally.

    planet_ids : 1D tensor of length N containing the actual planet IDs
                 present in this graph. Used to place scores into the
                 correct slots of the 40-dim output vectors.
    """

    def __init__(self, hidden_dim=64):
        super().__init__()

        # Source scorer: one number per node — "how good is this planet
        # as a source?" Shared linear projection over node embeddings.
        self.source_scorer = nn.Linear(hidden_dim, 1)

        # Target scorer: same idea, separate weights.
        # Source and target are different roles so they get different projections.
        self.target_scorer = nn.Linear(hidden_dim, 1)

        # Ship fraction head: global state → 10 bin logits
        # Two-layer MLP — the global mean pool loses per-node detail, so
        # a small MLP gives the head capacity to recover useful signal.
        self.ship_head = nn.Sequential(
            nn.Linear(hidden_dim, 40),
            nn.ELU(),
            nn.Linear(40, N_SHIP_BINS),
        )

        # Value head: global state → scalar
        # Same pattern as ship head — global pool then MLP.
        self.value_head = nn.Sequential(
            nn.Linear(hidden_dim, 40),
            nn.ELU(),
            nn.Linear(40, 1),
        )

    def forward(self, h, planet_ids):
        """
        h          : [N, 64]  node embeddings from encoder
        planet_ids : [N]      integer tensor of planet IDs (0–31)

        Returns a dict with keys:
            source_logits : [40]
            target_logits : [40]
            ship_logits   : [10]
            value         : scalar
        """
        N = h.shape[0]

        # ── per-node scores ──────────────────────────────────────────────────
        src_scores = self.source_scorer(h).squeeze(-1)   # [N]
        tgt_scores = self.target_scorer(h).squeeze(-1)   # [N]

        # ── scatter into fixed 40-slot vectors ──────────────────────────────
        # Start with a large negative number so empty slots never get selected
        source_logits = torch.full((N_PLANETS,), fill_value=-1e9, device=h.device)
        target_logits = torch.full((N_PLANETS,), fill_value=-1e9, device=h.device)

        # planet_ids tells us which slot each node belongs to
        source_logits[planet_ids] = src_scores
        target_logits[planet_ids] = tgt_scores

        # ── global mean pool ────────────────────────────────────────────────
        # Average all node embeddings → [64] global state representation
        global_h = h.mean(dim=0)                         # [64]

        # ── ship fraction and value ──────────────────────────────────────────
        ship_logits = self.ship_head(global_h)           # [10]
        value       = self.value_head(global_h)          # [1]

        return {
            "source_logits": source_logits,
            "target_logits": target_logits,
            "ship_logits"  : ship_logits,
            "value"        : value.squeeze(-1),          # scalar
        }

