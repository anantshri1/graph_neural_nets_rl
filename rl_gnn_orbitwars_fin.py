"""Cross-entropy loss for classification takes two inputs: a vector of raw logits (our source or target logits over 40 slots) and an integer label (the correct planet ID). Internally it applies softmax to the logits to get probabilities, then computes -log(probability of the correct class). So if planet 5 is the correct source and the model assigns it probability 0.9, loss = -log(0.9) = 0.10. If it assigns 0.1, loss = -log(0.1) = 2.30. The loss pushes the model to assign higher probability to the correct planet.

>One subtlety: our logits have -1e9 for inactive planet slots. Softmax of -1e9 is essentially 0, so those slots never contribute to the loss. They're effectively masked out automatically.
"""

