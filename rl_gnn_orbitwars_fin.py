"""The GNN encoder takes a graph, outputs a per-node embedding vector. This is the "understanding" part of the network. We'll use ``GATv2Conv`` (Graph Attention Network v2) from ``PyTorch Geometric``.

**Why ``GATv2Conv``**:

The original GAT computes attention weights as:

<center>
  $\alpha_{ij}$ = softmax( $a^T$ · LeakyReLU( $W·h_i$ || $W·h_j$ ) )
</center>

The problem is that `W·h_i` and `W·h_j` are computed before they interact — so the attention is actually a function of `W·h_i` alone when you factor it. GATv2 fixes this by concatenating before the linear transform:

<center>
  $\alpha_{ij}$ = softmax( $a^T$ · LeakyReLU($W$ · $[h_i$ || $h_j$] ) )
</center>

Now the attention weight genuinely depends on the pair `(i, j)`, not just `i`. For OrbitWars this matters: whether planet A should "pay attention" to planet B depends on both of them — their relative ownership, ship counts, and proximity all interact.


"""

import torch
import torch.nn as nn
from torch_geometric.nn import GATv2Conv

class OrbitWarsGNNEncoder(nn.Module):
    """
    3-layer GATv2 encoder.

    Input : torch_geometric Data with x=[N,6], edge_attr=[E,4]
    Output: node embeddings of shape [N, hidden_dim]

    Each GATv2Conv layer does:
      1. For each edge (i→j): concat h_i and h_j, apply shared linear W,
         then LeakyReLU, then dot with attention vector a → scalar weight α_ij
      2. Aggregate: h_i_new = ELU( Σ_j α_ij · W_val · h_j )
      3. Multi-head: run H independent heads, concat their outputs

    We use edge_attr as additional input to the attention — the GATv2Conv
    'edge_dim' argument lets the layer include edge features in the
    attention score computation. This means proximity and ownership
    information on edges directly influences which neighbours get attended to.
    """

    def __init__(self, node_feat_dim=6, edge_feat_dim=4, hidden_dim=64, n_heads=4, n_layers=3):
        super().__init__()

        self.hidden_dim = hidden_dim

        # Layer 1: input projection
        # in_channels  = node_feat_dim (6)
        # out_channels = hidden_dim // n_heads (16 per head × 4 heads = 64 total)
        # concat=True means outputs of all heads are concatenated → hidden_dim
        self.conv1 = GATv2Conv(
            in_channels  = node_feat_dim,
            out_channels = hidden_dim // n_heads,
            heads        = n_heads,
            edge_dim     = edge_feat_dim,
            concat       = True,
        )

        # Layer 2: hidden → hidden
        # input is now hidden_dim (64) because layer 1 concatenated 4 heads
        self.conv2 = GATv2Conv(
            in_channels  = hidden_dim,
            out_channels = hidden_dim // n_heads,
            heads        = n_heads,
            edge_dim     = edge_feat_dim,
            concat       = True,
        )

        # Layer 3: hidden → hidden, same dimensions
        self.conv3 = GATv2Conv(
            in_channels  = hidden_dim,
            out_channels = hidden_dim // n_heads,
            heads        = n_heads,
            edge_dim     = edge_feat_dim,
            concat       = True,
        )

        self.activation = nn.ELU()

    def forward(self, x, edge_index, edge_attr):
        """
        x          : [N, 6]    node features
        edge_index : [2, E]    COO edge connectivity
        edge_attr  : [E, 4]    edge features

        Returns    : [N, 64]   node embeddings
        """
        # Layer 1
        h = self.conv1(x, edge_index, edge_attr)
        h = self.activation(h)            # [N, 64]

        # Layer 2 — residual connection: add input to output
        # Residual helps gradients flow back through deep networks
        h = self.conv2(h, edge_index, edge_attr) + h
        h = self.activation(h)            # [N, 64]

        # Layer 3 — residual again
        h = self.conv3(h, edge_index, edge_attr) + h
        h = self.activation(h)            # [N, 64]

        return h                          # [N, 64]

