from collections import namedtuple

Planet = namedtuple('Planet', ['id', 'owner', 'x', 'y', 'radius', 'ships', 'production'])

def heuristic_agent(obs, config):
    """
    Main agent function compatible with kaggle-environments.

    obs    : observation object from the environment
    config : config object (we use config.episodeSteps if needed)

    Returns a list of actions: [[source_id, angle_radians, ships_to_send], ...]
    """

    my_player_id = obs.player
    current_turn = getattr(obs, 'step', 0)
    initial_planets = obs.initial_planets
    angular_velocity = obs.angular_velocity

    # --- Step 1: Parse planets ---
    planets = [
        Planet(
            id=p[0],
            owner=p[1],
            x=p[2],
            y=p[3],
            radius=p[4],
            ships=p[5],
            production=p[6]
        )
        for p in obs.planets
    ]

    # --- Step 2: Split by ownership ---
    my_planets = [p for p in planets if p.owner == my_player_id]
    other_planets = [p for p in planets if p.owner != my_player_id]

    # Nothing to do if we have no planets
    if not my_planets or not other_planets:
        return []

    # --- Step 3: Get assignments ---
    assignments = assign_targets(
        my_planets, other_planets, my_player_id,
        current_turn, initial_planets, angular_velocity
    )

    # --- Step 4: Build action list ---
    actions = []

    for src in my_planets:
        if src.id not in assignments:
            continue  # idle planet — no assignment found

        tgt, ships_to_send = assignments[src.id]

        # Compute intercept point (aim at where planet WILL be, not where it is)
        ix, iy, eta = intercept_point(
            src.x, src.y,
            tgt.id,
            ships_to_send,
            current_turn,
            initial_planets,
            angular_velocity
        )

        # Get a sun-safe angle toward the intercept point
        angle = safe_angle(src.x, src.y, ix, iy)

        if angle is None:
            # No safe path exists — skip this action rather than fly into the sun
            continue

        # Guard: never send 0 ships
        if ships_to_send <= 0:
            continue

        actions.append([src.id, angle, ships_to_send])

    return actions

