def build_imitation_model(input_dim=183, n_planets=36):
    inputs = Input(shape=(input_dim,))

    # shared trunk
    x = Dense(256, activation='relu')(inputs)
    x = BatchNormalization()(x)
    x = Dropout(0.3)(x)

    x = Dense(128, activation='relu')(x)
    x = BatchNormalization()(x)
    x = Dropout(0.2)(x)

    x = Dense(128, activation='relu')(x)
    x = BatchNormalization()(x)
    x = Dropout(0.2)(x)

    x = Dense(64, activation='relu')(x)

    # output heads
    source_out = Dense(n_planets, activation='softmax', name='source')(x)
    sin_out    = Dense(1, activation='tanh', name='sin_angle')(x)  # tanh clips to [-1,1]
    cos_out    = Dense(1, activation='tanh', name='cos_angle')(x)
    ships_out  = Dense(1, activation='sigmoid', name='ships')(x)   # sigmoid clips to [0,1]

    model = Model(inputs=inputs, outputs=[source_out, sin_out, cos_out, ships_out])
    return model

