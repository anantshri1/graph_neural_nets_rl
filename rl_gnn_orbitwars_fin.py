print(f"Source ID range : {source_ids.min().item()} – {source_ids.max().item()}")
print(f"Target ID range : {target_ids.min().item()} – {target_ids.max().item()}")

"""Cross-entropy in PyTorch expects `(logits [batch, n_classes], labels [batch])`. Our logits come out of `_get_logits_and_value` as `[batch, 40]` — exactly right. The labels are our source_ids and target_ids integer tensors."""

# ── Block 5: warm-up training loop ───────────────────────────────────────────

N_EPOCHS   = 10     # full passes over the 2000 samples
BATCH_SIZE = 64

n_samples  = features.shape[0]
ce_loss_fn = torch.nn.CrossEntropyLoss()

print(f"Training {N_EPOCHS} epochs × {n_samples // BATCH_SIZE} batches = "
      f"{N_EPOCHS * (n_samples // BATCH_SIZE)} total updates\n")

for epoch in range(N_EPOCHS):
    # shuffle each epoch so batches are different each time
    perm = torch.randperm(n_samples)
    epoch_src_loss = 0.0
    epoch_tgt_loss = 0.0
    n_batches = 0

    for start in range(0, n_samples, BATCH_SIZE):
        idx = perm[start : start + BATCH_SIZE]

        obs_batch = features[idx]       # [64, 243]
        src_batch = source_ids[idx]     # [64]
        tgt_batch = target_ids[idx]     # [64]

        # forward pass — get logits (values unused during warm-up)
        src_logits, tgt_logits, _, _ = model.policy._get_logits_and_value(obs_batch)
        # src_logits : [64, 40]
        # tgt_logits : [64, 40]

        # cross-entropy loss
        # pushes src_logits[i, source_ids[i]] to be high
        # pushes tgt_logits[i, target_ids[i]] to be high
        src_loss = ce_loss_fn(src_logits, src_batch)
        tgt_loss = ce_loss_fn(tgt_logits, tgt_batch)
        loss     = src_loss + tgt_loss

        warmup_optimizer.zero_grad()
        loss.backward()
        warmup_optimizer.step()

        epoch_src_loss += src_loss.item()
        epoch_tgt_loss += tgt_loss.item()
        n_batches += 1

    print(f"Epoch {epoch+1:2d} — "
          f"src_loss: {epoch_src_loss/n_batches:.4f}  "
          f"tgt_loss: {epoch_tgt_loss/n_batches:.4f}  "
          f"total: {(epoch_src_loss+epoch_tgt_loss)/n_batches:.4f}")

"""`src_loss` is decreasing steadily: 2.92 → 1.87. Starting near log(40) = 3.69 would mean completely uniform — we started at 2.92 which means the encoder had already given the source scorer some signal to work with. The decrease to 1.87 means the source scorer is learning to prefer owned planets.

`tgt_loss` is completely flat: 3.32 across all 10 epochs. This is the problem. Target scorer is not learning at all.

The target scorer needs to learn "prefer unowned planets." But our synthetic labels pick a random unowned planet as the target — not a specific one that's strategically best. With 20-35 unowned planets per game and a random label, the correct target could be any of them. The cross-entropy loss is trying to push one specific random planet's logit up, while suppressing all other unowned planets — including ones that are equally valid targets.
The source problem is easier: there are only 1-4 owned planets per game, so the signal is less noisy. A random owned planet as the source label is usually reasonable.
For the target, the random label provides contradictory signal across batches — "planet 5 is the target" in one sample, "planet 12 is the target" in another, even for nearly identical game states. The scorer can't learn a consistent pattern.

Instead of a single random target label, we should label all unowned planets as valid targets and use a different loss. Binary cross-entropy per slot works better here:

* Owned planets: label 0 (should not be targeted)
* Unowned planets: label 1 (valid targets)

"""

# ── Block 6: add target masks to warmup data ─────────────────────────────────

def add_target_masks(warmup_data):
    """
    For each sample, compute a boolean mask of length 40 where:
        True  = this planet slot is a valid target (unowned, active)
        False = owned by us, or inactive slot

    Replaces the single target_id label with a multi-label mask.
    """
    updated = []

    for d in warmup_data:
        features = d["features"]   # [243]

        # reshape planet slots: [40, 6]
        planet_slots = features[:240].reshape(40, 6)

        # feature index 5 = is_active, feature index 2 = owner_enc
        # owner_enc: 0=neutral, 1=mine, 2=opponent
        is_active  = planet_slots[:, 5] > 0.5    # [40] bool
        is_mine    = planet_slots[:, 2] == 1.0   # [40] bool

        # valid target: exists in this game AND not owned by us
        target_mask = is_active & ~is_mine        # [40] bool

        updated.append({
            "features"    : d["features"],
            "source_id"   : d["source_id"],
            "target_mask" : target_mask.astype(np.float32),  # [40] float
        })

    return updated

