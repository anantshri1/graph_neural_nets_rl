"""# **Building a Physics Informed Heuristic Agent**"""

import math

def score_target(source_planet, target_planet, my_player_id,
                 current_turn, initial_planets, angular_velocity,
                 ships_to_send = None):
    """
    Returns a float score for sending a fleet from source to target.
    Higher score = more desirable action.

    source_planet, target_planet: Planet named tuples
    my_player_id: int so we know (enemy or neutral)
    ships_to_send: if None, half of source's ships are sent
    """

    if ships_to_send is None:
        ships_to_send = max(1, source_planet.ships // 2)

    # ------------------ ETA to intercept ponts -----------
    ix, iy, eta = intercept_point(source_planet.x, source_planet.y,
                                  target_planet.id, ships_to_send,
                                  current_turn, initial_planets,
                                  angular_velocity)

    # penalize long travel times
    time_score = 1.0 / eta

    # -------- Affordability of target (> 1, can take it) ---
    # neutral, enemy -> need target.ships + 1
    # ours -> not allowed

    ships_needed = target_planet.ships + 1
    affordability = ships_to_send / max(ships_needed, 1)

    # ------- Production value -------------
    prod_score = target_planet.production / 5.0

    # ---------- combine -----------------------
    score = (time_score) * min(affordability, 2.0)

    return score

    # ---------- Design Notes -----------------
    # production scoring hurts early game — pure time + affordability is better

def assign_targets(my_planets, enemy_and_neutral_planets, my_player_id,
                   current_turn, initial_planets, angular_velocity,
                   ship_fraction=0.9):
    """
    Assigns each of our planets a target to send ships to.

    Returns a dict: {source_planet.id: (target_planet, ships_to_send)}

    my_planets        : list of Planets we own
    enemy_and_neutral_planets: list of Planets we don't own
    ship_fraction     : fraction of ships to send (default 80%); keep some for defence

    """

    assignments = {} # source_id -> (target_planet, ships_to_send)

    # Track how many times each target has been assiged so we don't ignore pile-on
    target_assignment_count = {}

    # Build a flat list of all (score, source, target, ships)
    candidates = []

    for src in my_planets:
        ships_to_send = max(1, int(src.ships * ship_fraction))

        for tgt in enemy_and_neutral_planets:
          s = score_target(src, tgt, my_player_id,
                           current_turn, initial_planets, angular_velocity,
                           ships_to_send)
          candidates.append((s, src, tgt, ships_to_send))

      # best opportunities first
    candidates.sort(key=lambda c: c[0], reverse=True)
    assigned_sources = set()

    for score, src, tgt, ships_to_send in candidates:
      # skip if already assigned
        if src.id in assigned_sources:
          continue

        if target_assignment_count.get(tgt.id,0)>=2:
          continue

        assignments[src.id] = (tgt, ships_to_send)
        assigned_sources.add(src.id)
        target_assignment_count[tgt.id] = target_assignment_count.get(tgt.id, 0) + 1

    return assignments

def safe_angle(src_x, src_y, target_x, target_y,
               sun_x=50, sun_y=50, sun_radius=10,
               nudge_deg = 5, max_attempts = 36):
  """
  Returns a safe angle (in radians) from source to the target, while avoiding the sun

  If the direct path is clear, returns the direct angle unchanged. Otherwise,
  tries nudging left and right in increments of nudge_de until a clear path is found

  None -> no safe angle found
  """


  direct_angle = math.atan2(target_y - src_y, target_x - src_x)

  # Fast path
  if not hits_sun(src_x, src_y, target_x, target_y, sun_x, sun_y, sun_radius):
      return direct_angle
  nudge_rad = math.radians(nudge_deg)

  for i in range(1, max_attempts +1):
      for sign in (+1, -1):
          angle = direct_angle + sign * i * nudge_rad

          dist = 200
          end_x = src_x + dist * math.cos(angle)
          end_y = src_y + dist * math.sin(angle)

          if not hits_sun(src_x, src_y, end_x, end_y, sun_x, sun_y, sun_radius):
              return angle
  return None

