"""**Potential Fix**

Instead of labels:
```
tgt_probs = softmax(tgt_logits)
loss = -log(sum probs over valid targets)
```

which matches any valid target is correct.

More precisely we assign probability mass to the valid set, and maximise likelihood of the expert choice within it:

<center>
$L$ = -log $\sum_{a\in A_{valid}}$ $\pi (a|s)$
</center>

"""

for epoch in range(N_EPOCHS):
    perm = torch.randperm(n_samples_v2)

    epoch_src_loss = 0.0
    epoch_tgt_loss = 0.0
    n_batches = 0

    for start in range(0, n_samples_v2, BATCH_SIZE):
        idx = perm[start:start + BATCH_SIZE]

        obs_batch = features_v2[idx]
        src_batch = source_ids_v2[idx]
        tgt_mask  = target_masks_v2[idx].bool()   # IMPORTANT: boolean mask

        # forward
        src_logits, tgt_logits, _, _ = model.policy._get_logits_and_value(obs_batch)

        # -------------------
        # SOURCE LOSS (CE)
        # -------------------
        src_loss = F.cross_entropy(src_logits, src_batch)

        # -------------------
        # TARGET LOSS (Option B: set likelihood)
        # -------------------
        log_probs = F.log_softmax(tgt_logits, dim=-1)

        # mask invalid actions
        log_probs = log_probs.masked_fill(~tgt_mask, -1e9)

        # log probability of ANY valid action
        tgt_loss = -torch.logsumexp(log_probs, dim=-1).mean()

        # -------------------
        # TOTAL LOSS
        # -------------------
        loss = src_loss + tgt_loss

        warmup_optimizer.zero_grad()
        loss.backward()
        warmup_optimizer.step()

        epoch_src_loss += src_loss.item()
        epoch_tgt_loss += tgt_loss.item()
        n_batches += 1

